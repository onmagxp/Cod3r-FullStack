function encontrarMaiorNumero(a, b) {
    if (a > b) {
        return a
    } else {
        return b
    }
}

console.log(encontrarMaiorNumero(10, 15))
console.log(encontrarMaiorNumero(7, 5))
console.log(encontrarMaiorNumero(20, 10))
console.log(encontrarMaiorNumero(15, 15))
console.log(encontrarMaiorNumero(30, 40))